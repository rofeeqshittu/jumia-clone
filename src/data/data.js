// Additional utility functions
export const formatCurrencyNGN = (value) => {
  try {
    return new Intl.NumberFormat("en-NG", { 
      style: "currency", 
      currency: "NGN", 
      maximumFractionDigits: 0 
    }).format(value)
  } catch {
    return `₦ ${value.toLocaleString()}`
  }
}

